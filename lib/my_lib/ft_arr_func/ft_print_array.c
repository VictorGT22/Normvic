#include "libft.h"

void	print_array(char **arr, char end_char)
{
	int	i;

	i = 0;
	while (arr[i])
	{
		write(1, arr[i], ft_strlen(arr[i]));
		write(1, &end_char, 1);
		i++;
	}
}
