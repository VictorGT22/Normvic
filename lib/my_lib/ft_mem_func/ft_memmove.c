#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	char	*temp;
	char	*dest;

	temp = (char *)src;
	dest = (char *)dst;
	if (temp < dest)
	{
		while (n--)
			((unsigned char *)dst)[n] = ((unsigned char *)src)[n];
	}
	else
		ft_memcpy(dest, temp, n);
	return (dst);
}
