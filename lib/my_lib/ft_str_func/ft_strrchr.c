#include "libft.h"

char	*ft_strrchr(const char *str, char c)
{
	char	*ptr;

	ptr = NULL;
	while (*str != '\0')
	{
		if (*str == c)
			ptr = (char *)str;
		str++;
	}
	if (c == '\0')
		return ((char *)str);
	return (ptr);
}
