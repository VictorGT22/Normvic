#include "libft.h"

int	ft_empty_until_pos(char *str, int pos)
{
	int	i;

	i = 0;
	while (str[i] && i <= pos)
	{
		if (str[i] != ' ' && str[i] != '\t')
			return (0);
		i++;
	}
	return (1);
}
